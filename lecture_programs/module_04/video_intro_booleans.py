# Boolean Values
# True  | 1 | On  | Yes
# False | 0 | Off | No

is_raining = True
print(is_raining, type(is_raining)) # Output: True

if is_raining:
    print("It's raining men")

is_sunny = False
print(is_sunny) # Output: False

if not is_sunny:
    print("Stay inside")
